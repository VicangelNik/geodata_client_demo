package gr.uoa.di.model;

public class Distance {

  private double lat;
  private double lon;
  private int km;

  public double getLat() {
    return lat;
  }

  public void setLat(double lat) {
    this.lat = lat;
  }

  public double getLon() {
    return lon;
  }

  public void setLon(double lon) {
    this.lon = lon;
  }

  public int getKm() {
    return km;
  }

  public void setKm(int km) {
    this.km = km;
  }
}
